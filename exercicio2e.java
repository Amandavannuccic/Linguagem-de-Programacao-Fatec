public class TrocaVariaveis {
    public static void main(String[] args) {
        // Inicialize A com 10 e B com 5
        int A = 10;
        int B = 5;
        
        // Calcule C como a soma de A e B
        int C = A + B;
        
        // Atualize B para 20 e A para 10
        B = 20;
        A = 10;
        
        // Imprima os valores de A, B e C
        System.out.println("A: " + A);
        System.out.println("B: " + B);
        System.out.println("C: " + C);
    }
}
